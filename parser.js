module.exports = function(accounts, cb){
  var accountsNumbers = [];

  var account = "";
  for(i = 0; i < accounts.length; i++){
    for(y = 0; y < accounts[i].length; y++){
      for(x = 0; x < numbers.length; x++){
        if(JSON.stringify(numbers[x]) === JSON.stringify(accounts[i][y])){
          account += x.toString();
          x = numbers.length;
        }
      }
    }
    accountsNumbers.push(account);
    account = "";
  }

  cb(accountsNumbers);
};
